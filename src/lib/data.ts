// Função para determinar preposição correta
const getPreposicao = (nome: string): string => {
  const femininos = ['Agronomia', 'Auxiliadora', 'Azenha', 'Cavalhada', 'Cascata', 
    'Serraria', 'Teresópolis', 'Tristeza', 'Glória', 'Hípica', 'Independência',
    'Medianeira', 'Restinga', 'Serraria'];
  
  if (femininos.includes(nome)) return 'na';
  if (nome.startsWith('Vila ')) return 'na';
  if (nome.endsWith('a') || nome.endsWith('ção') || nome.endsWith('dade')) return 'na';
  
  return 'no';
};

